function WaveShield:getItemsToWhiteList(resourceName, type)
    if autoWhiteListTable[resourceName] then
        return autoWhiteListTable[resourceName][type] or {}
    else
        return {}
    end
end

function WaveShield:runAutoWhiteList(table, type)
    local tempTable = table
    local items = WaveShield:getItemsToWhiteList("default", type)
    for k,v in pairs(items) do
        tempTable[GetHashKey(v)] = true
    end

    if type == "Props" and not WaveShield.Config.Entities.EnableObjectsWhiteList then return tempTable end
    if type == "Peds" and not WaveShield.Config.Entities.EnablePedsWhiteList then return tempTable end
    if type == "Vehicles" and not WaveShield.Config.Entities.EnableVehiclesWhiteList then return tempTable end
    if type == "Particles" and not WaveShield.Config.Explosions.EnableParticlesWhiteList then return tempTable end

    for i = 0, GetNumResources() - 1 do
        local resourceName = GetResourceByFindIndex(i)
        if autoWhiteListTable[resourceName] then
            local resourceState = GetResourceState(resourceName)
            if resourceState == "started" then
                local items2 = WaveShield:getItemsToWhiteList(resourceName, type)
                for k,v in pairs(items2) do
                    tempTable[GetHashKey(v)] = true
                end
            end
        end
    end
    return tempTable
end

function WaveShield:runAutoBlackList()
    local tempTable = {}

    for _, modelName in pairs(autoBlackListTable) do
        local modelHash = (type(modelName) == "string" and GetHashKey(modelName)) or (type(modelName) == "number" and modelName) or tonumber(modelName)
        if modelHash then
            tempTable[modelHash] = true
        end
    end

    return tempTable
end
