WaveShield.CreateThread(function()
    for _, pickup in WaveShield.Lua.pairs(allPickups) do
        table.insert(allPickupsHashes, WaveShield.Native.GetHashKey(pickup))
    end
end)

local checkPickups = function()
    if not WaveShield.Config.Entities.AntiPickupSpawn then
        return
    end

    for _, pickup in WaveShield.Lua.pairs(allPickupsHashes) do
        ToggleUsePickupsForPlayer(WaveShield.playerId, pickup, false)
    end
end)

WaveShield.RegisterDetection("pickups", checkPickups, 10000)